public class Coba1 {
        public static void main(String[] args) {
            int x = 74;

            if (x >= 85 && x <= 100) {
                System.out.println("A");
            } else if (x >= 75 && x < 85) {
                System.out.println("B");
            } else if (x >= 65 && x < 75) {
                System.out.println("C");
            } else if (x >= 55 && x < 65) {
                System.out.println("D");
            } else {
                System.out.println("E");
            }
        }
}
